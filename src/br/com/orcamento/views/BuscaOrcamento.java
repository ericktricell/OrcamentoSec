    private void preencheLista(){
        listOrc = new OrcamentoDAO().listOrc();
        
        this.organizaTable();
    }
    
    private void organizaTable(){
        DefaultTableModel model = new DefaultTableModel();
        
        model.addColumn("NumDoc");
        model.addColumn("Cliente");
        model.addColumn("Data");
        model.addColumn("Valor");
        
        for (Orcamento o : listOrc){
            model.addRow(new String[]{
                o.getNumDoc(),
                o.getCliente().getNome(),
                new SimpleDateFormat("dd/MM/yyyy").format(o.getData()),
                new DecimalFormat("Â¤#,##0.00").format(o.getVlrTotal())
            });
        }
        
        jTable1.setModel(model);
    }
    
    private void setAcessibilidade() {
        JRootPane meurootpane = getRootPane();
        meurootpane.getInputMap(JComponent.WHEN_IN_FOCUSED_WINDOW).put(KeyStroke.getKeyStroke(KeyEvent.VK_ESCAPE, 0), "ESCAPE");
        meurootpane.getRootPane().getActionMap().put("ESCAPE", new AbstractAction("ESCAPE") {
            @Override
            public void actionPerformed(ActionEvent e) {
                int i = JOptionPane.showConfirmDialog(null, "Deseja realmente sair?");
                if (i == 0){
                    dispose();
                }
                
            }
        });
    }
