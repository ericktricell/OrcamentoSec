    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        cli = listCli.get(jTable1.getSelectedRow());
        nomeFD.setText(cli.getNome());
        telefoneFD.setText(cli.getTelefone());
        logradouroFD.setText(cli.getLogradouro());
        numFD.setText(String.valueOf(cli.getNum()));
        bairroFD.setText(cli.getBairro());
        cidadeFD.setText(cli.getCidade());
        cepFD.setText(cli.getCep());
        
        for (int i = 0; i <= ufCB.getItemCount(); i++){
            ufCB.setSelectedIndex(i);
            if(ufCB.getSelectedItem().equals(cli.getUf())){
                ufCB.setSelectedIndex(i);
                break;
            }
        }
    }//GEN-LAST:event_jTable1MouseClicked

