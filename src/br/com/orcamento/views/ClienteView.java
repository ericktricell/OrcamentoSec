    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        cli = new Cliente();
        cli.setNome(nomeFD.getText());
        cli.setTelefone(telefoneFD.getText());
        cli.setLogradouro(logradouroFD.getText());
        cli.setNum(num);
        cli.setBairro(bairroFD.getText());
        cli.setCidade(cidadeFD.getText());
        cli.setCep(cepFD.getText());
        cli.setUf(ufCB.getSelectedItem().toString());
        
        if(new ClienteDAO().salvar(cli)){
            JOptionPane.showMessageDialog(null, "Salvo com sucesso");
            this.preencheTable();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void numFDFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_numFDFocusLost
        try{
            num = Integer.parseInt(numFD.getText());
        }catch(NumberFormatException e){
            JOptionPane.showMessageDialog(null, "Digite apenas numeros");
            numFD.setText("");
            numFD.requestFocus();
        }
    }//GEN-LAST:event_numFDFocusLost

